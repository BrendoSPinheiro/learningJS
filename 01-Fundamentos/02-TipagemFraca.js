/**
 * As variáveis do javascript podem ter seus tipos modificados durante o código
 */

let informacaoPessoa = 'Brendo';

console.log(typeof informacaoPessoa); // imprime: String

informacaoPessoa = 20;

console.log(typeof informacaoPessoa); // imprime: Number

informacaoPessoa = true;

console.log(typeof informacaoPessoa); // imprime: Boolean
